#include "ft_printf.h"

void	ft_k(int fd, t_info *i)
{
	if (i->y)
	{
		write(i->g, i->x, i->y);
		i->g = fd;
		i->w += i->y;
		i->y = 0;
		i->z += i->y;
		free(i->x);
	}
}
