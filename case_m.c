#include "ft_printf.h"

static void	ft_m3(const char *s, t_info *i)
{
	if (*s == 'B' && *(s + 1) == 'O' && *(s + 2) == 'L' && *(s + 3) == 'D')
		ft_inits("\033[1m", i);
	else if (*s == 'I' && *(s + 1) == 'T' && *(s + 2) == 'A' && *(s + 3) == 'L'
		&& *(s + 4) == 'I' && *(s + 5) == 'C')
		ft_inits("\033[3m", i);
	else if (*s == 'U' && *(s + 1) == 'N' && *(s + 2) == 'D' && *(s + 3) == 'E'
		&& *(s + 4) == 'R' && *(s + 5) == 'L' && *(s + 5) == 'I' && *(s + 5)
		== 'N' && *(s + 5) == 'E')
		ft_inits("\033[4m", i);
	else if (*s == 'S' && *(s + 1) == 'W' && *(s + 2) == 'A' && *(s + 3) == 'P')
		ft_inits("\033[7m", i);
}

static void	ft_m2(const char *s, t_info *i)
{
	if (*s == 'B' && *(s + 1) == 'L' && *(s + 2) == 'A' && *(s + 3) == 'C' &&
		*(s + 4) == 'K')
		ft_inits("\033[40m", i);
	else if (*s == 'R' && *(s + 1) == 'E' && *(s + 2) == 'D')
		ft_inits("\033[41m", i);
	else if (*s == 'G' && *(s + 1) == 'R' && *(s + 2) == 'E' && *(s + 3) == 'E'
		&& *(s + 4) == 'N')
		ft_inits("\033[42m", i);
	else if (*s == 'B' && *(s + 1) == 'R' && *(s + 2) == 'O' && *(s + 3) == 'W'
		&& *(s + 4) == 'N')
		ft_inits("\033[43m", i);
	else if (*s == 'B' && *(s + 1) == 'L' && *(s + 2) == 'U' && *(s + 3) == 'E')
		ft_inits("\033[44m", i);
	else if (*s == 'M' && *(s + 1) == 'A' && *(s + 2) == 'G' && *(s + 3) == 'E'
		&& *(s + 4) == 'N' && *(s + 5) == 'T' && *(s + 6) == 'A')
		ft_inits("\033[45m", i);
	else if (*s == 'C' && *(s + 1) == 'Y' && *(s + 2) == 'A' && *(s + 3) == 'N')
		ft_inits("\033[46m", i);
	else if (*s == 'G' && *(s + 1) == 'R' && *(s + 2) == 'E' && *(s + 3) == 'Y')
		ft_inits("\033[47m", i);
	else if (*s == '*')
		ft_m3(s - 1, i);
}

void		ft_m(const char *s, t_info *i)
{
	if (*s == 'N' && *(s + 1) == 'O' && *(s + 2) == 'N' && *(s + 3) == 'E')
		ft_inits("\033[00m", i);
	else if (*s == 'B' && *(s + 1) == 'L' && *(s + 2) == 'A' && *(s + 3) == 'C'
		&& *(s + 4) == 'K')
		ft_inits("\033[30m", i);
	else if (*s == 'R' && *(s + 1) == 'E' && *(s + 2) == 'D')
		ft_inits("\033[31m", i);
	else if (*s == 'G' && *(s + 1) == 'R' && *(s + 2) == 'E' && *(s + 3) == 'E'
		&& *(s + 4) == 'N')
		ft_inits("\033[32m", i);
	else if (*s == 'B' && *(s + 1) == 'R' && *(s + 2) == 'O' && *(s + 3) == 'W'
		&& *(s + 4) == 'N')
		ft_inits("\033[33m", i);
	else if (*s == 'B' && *(s + 1) == 'L' && *(s + 2) == 'U' && *(s + 3) == 'E')
		ft_inits("\033[34m", i);
	else if (*s == 'M' && *(s + 1) == 'A' && *(s + 2) == 'G' && *(s + 3) == 'E'
		&& *(s + 4) == 'N' && *(s + 5) == 'T' && *(s + 6) == 'A')
		ft_inits("\033[35m", i);
	else if (*s == 'C' && *(s + 1) == 'Y' && *(s + 2) == 'A' && *(s + 3) == 'N')
		ft_inits("\033[36m", i);
	else if (*s == 'G' && *(s + 1) == 'R' && *(s + 2) == 'E' && *(s + 3) == 'Y')
		ft_inits("\033[37m", i);
	else if (*s == '*')
		ft_m2(s + 1, i);
}
